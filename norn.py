def find_by_numeric_condition(parameter_name, min_value=None, max_value=None, unit="°C"):
    """Ищет объекты, у которых физ. параметр находится в заданных границах."""
    results = []
    for obj in onto.individuals():
        if hasattr(obj, "has_condition"):
            for param in obj.has_condition:
                if param.param_name == parameter_name and param.param_unit == unit:
                    val = param.param_value
                    is_match = True
                    if min_value is not None and val <= min_value:
                        is_match = False
                    if max_value is not None and val >= max_value:
                        is_match = False
                    if is_match:
                        results.append((obj, val))
    return results

