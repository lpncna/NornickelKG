    class param_value(DataProperty, FunctionalProperty):
        domain = [Parameter]
        range = [float]  # Числовое значение (поддерживает и int, и float)

    class param_unit(DataProperty, FunctionalProperty):
        domain = [Parameter]
        range = [str]  # Например: "°C", "кПа", "МПа"

    # ==========================================
    # ОБНОВЛЕННЫЕ СВОЙСТВА ОБЪЕКТОВ ДЛЯ КЛАССОВ
    # ==========================================
    # Вместо DataProperty (списка строк) делаем ObjectProperty,
    # ведущее к нашему новому классу Parameter
    class has_condition(ObjectProperty):
        domain = [Experiment | Process | Property]
        range = [Parameter]


